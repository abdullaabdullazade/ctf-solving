def decrypt(cypher):
    flat = []
    for part in cypher:
        for item in part:
            if isinstance(item, str) and item.startswith("0x"):
                flat.append(item)
            elif isinstance(item, list):  
                for x in item:
                    if isinstance(x, str) and x.startswith("0x"):
                        flat.append(x)
    return "".join([chr(int(h, 16)) for h in flat])


